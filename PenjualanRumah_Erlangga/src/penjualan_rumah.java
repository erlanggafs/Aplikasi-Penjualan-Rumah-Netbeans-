    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        randomID();
        txtNoPelanggan.setText(""+no_pelanggan);
        txtSetuju.setEnabled(true);
        Ltersedia = Integer.parseInt(txtLuasTanahT.getText());
        Htanah = Integer.parseInt(txtHargaTanahM2.getText());
        Hbangunan = Integer.parseInt(txtHargaBangunan.getText());
        int tanah = Htanah * Ltersedia;
        int total = tanah + Hbangunan;
        txtHarga.setText("" + total);
        int nPpn = total/100*10;
        txtPpn.setText(""+nPpn);
        nDP = Integer.parseInt(txtDp.getText());
        int bayar = total + nPpn-nDP;
        nPeriode = Integer.parseInt(txtPeriode.getText());
        int nBulan = nPeriode;
        int nCicilan = bayar/nBulan;
        txtCicilan.setText(""+ nCicilan);
    }//GEN-LAST:event_jButton1ActionPerformed

