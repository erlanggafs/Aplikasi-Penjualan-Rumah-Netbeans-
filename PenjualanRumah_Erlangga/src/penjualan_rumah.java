    public void randomID(){
        Random angkaRandom = new Random(); //objek random
            for (int i = 0; i < 5; i++) {
            randomA = (char) (angkaRandom.nextInt(26) + 'A');
            System.out.print(randomA);
            }
            for (int i = 0; i < 5; i++) {
            randomB = angkaRandom.nextInt(100) + 1;
            System.out.print(randomB);
            }
            no_pelanggan = "PO" + randomA + "_"+randomB;
    }
