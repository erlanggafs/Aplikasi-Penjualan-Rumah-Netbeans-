    private void jSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jSimpanActionPerformed
       try{
           Connection conn = koneksi.getConnection();
           PreparedStatement stmt = conn.prepareStatement("INSERT INTO "
           + "rumah(no_pelanggan,nama, area, harga_tanah, harga_bangunan, "
           + "tipe_rumah, luas_tanah_a,luas_tanah_t,harga, dp, periode, ppn, cicilan)"
           + "values(?,?,?,?,?,?,?,?,?,?,?,?,?)");
           
           stmt.setString(1, no_pelanggan);
           stmt.setString(2, txtNamaPel.getText());
           stmt.setString(3, area);
           stmt.setString(4, txtHargaTanahM2.getText());
           stmt.setString(5, txtHargaBangunan.getText());
           stmt.setString(6, tipe);
           stmt.setString(7, txtLuasTanahA.getText());
           stmt.setString(8, txtLuasTanahT.getText());
           stmt.setString(9, txtHarga.getText());
           stmt.setString(10, txtDp.getText());
           stmt.setString(11, txtPeriode.getText());
           stmt.setString(12, txtPpn.getText());
           stmt.setString(13, txtCicilan.getText());
           

           stmt.executeUpdate();
           no_pelanggan = "";
           txtNoPelanggan.setText("");
           txtNamaPel.setText("");
           comboArea.setSelectedIndex(0);
           txtHargaTanahM2.setText("");
           txtHargaBangunan.setText("");
           radio1.setSelected(false);
           radio2.setSelected(false);
           radio3.setSelected(false);
           txtLuasTanahA.setText("");
           txtLuasTanahT.setText("");
           txtHarga.setText("");
           txtDp.setText("");
           txtPeriode.setText("");
           txtPpn.setText("");
           txtCicilan.setText("");
           txtSetuju.setSelected(false);
           txtSetuju.setEnabled(false);
           JOptionPane.showMessageDialog(this, "Berhasil ! ");
       } catch(SQLException e){
           JOptionPane.showMessageDialog(this, e);
           System.out.print(e);
       }
    }//GEN-LAST:event_jSimpanActionPerformed

